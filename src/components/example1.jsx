import { useEffect } from 'react';

useEffect(() => {
  // Код, який виконується після рендеру
  console.log('Компонент змонтовано або оновлено!');

  // Функція очищення (опціонально)
  return () => {
    console.log('Компонент розмонтовано або перед оновленням');
  };
}, [/* масив залежностей */]);

